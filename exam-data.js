// Fungsi untuk generate dates dari format baru
function generateDatesFromRanges(ranges) {
  let dates = [];

  ranges.forEach((range) => {
    const start = new Date(range.start);

    // Handle single-day event (tanpa end date)
    if (!range.end) {
      dates.push(range.start);
      return;
    }

    // Validasi tanggal (contoh: June 31 -> July 1)
    let end = new Date(range.end);
    if (end.getDate() > new Date(end.getFullYear(), end.getMonth() + 1, 0).getDate()) {
      end = new Date(end.getFullYear(), end.getMonth() + 1, 1);
    }

    // Generate semua tanggal dalam range
    let current = new Date(start);
    while (current <= end) {
      dates.push(current.getFullYear() + "-" + String(current.getMonth() + 1).padStart(2, "0") + "-" + String(current.getDate()).padStart(2, "0"));
      current.setDate(current.getDate() + 1);
    }
  });

  return dates;
}

